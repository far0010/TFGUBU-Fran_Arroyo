-- seleccion de solicitantes contratados

SELECT
  NOM_SOL || ' ' || APE1_SOL || ' ' || APE2_SOL AS display_value,
  DNI_SOL AS return_value
FROM SOLICITANTE
JOIN CONTRATOS C ON C.CONTRATADO = DNI_SOL
ORDER BY NOM_SOL;

